//Programa que calcula o cubo de um número fornecido pelo usuário//
//Assunto: variáveis//
//Autor: Luciano Moraes da Luz Brum//
#include<stdio.h>
int main(void){
	int n, cubo;
	printf("Digite um numero\n");
	scanf("%d",&n);
	cubo=n*n*n;
	printf("\n\nO cubo deste numero e %d\n",cubo);
	return 0;
}


